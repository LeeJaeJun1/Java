package week2;

public class week2_2 {
    public static void main(String[] args) {
        byte b1 = 10;
        byte b2 = 20;
        int bSum = b1 + b2;
        System.out.println("bSum의 값: " + bSum);
        char c1 = 'A';
        char c2 = 1;
        int cSum = c1 + c2;
        System.out.println("cSum의 값: " + cSum);
        System.out.print("cSum의 문자: " + (char)cSum);
    }
}
